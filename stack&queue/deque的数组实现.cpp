#include "stdafx.h"
#define MAXSIZE 10


template<class T>
class CQueue
{
public:
	CQueue()
	{
		m_pData = new int[sizeof(int)*MAXSIZE];
		m_nfront = -1;
		m_nrear = -1;
	}
	~CQueue()
	{
		delete []m_pData;
		m_pData = NULL;
		m_nfront = -1;
		m_nrear = -1;
	}
public:

	void clearqueue()
	{
		m_nfront = -1;
		m_nrear = -1;
	}

	bool empty()
	{
		return (m_nfront==m_nrear) ? true:false;
	}

	int gettop()
	{
		if (empty())
		{
			return -1;
		}

		m_nfront++;
		return m_pData[m_nfront];
	}

	int size()
	{
		return m_nrear - m_nfront;
	}

	void push(int data)
	{
		if (m_nrear == MAXSIZE-1)
		{
			printf("队列已满，长度:%d\n", size());
			return;
		}

		m_nrear++;
		m_pData[m_nrear] = data;
		printf("%d ", data);
	}

	int pop()
	{
		if (m_nfront >= m_nrear)
		{
			printf("队列已空，长度:%d\n", size());
			return -1;
		}

		m_nfront++;
		int ret = m_pData[m_nfront];
		printf("%d ", ret);
		return ret;
	}
private:
	int *m_pData; //数据地址
	int m_nfront; // 队列头
	int m_nrear; // 队列尾
};

int _tmain(int argc, _TCHAR* argv[])
{
	CQueue<int> queue;

	// 入队
	queue.push(0);
	queue.push(1);
	queue.push(2);
	queue.push(3);
	queue.push(4);
	queue.push(5);
	queue.push(6);
	queue.push(7);
	queue.push(8);
	queue.push(9);
	queue.push(10);

	// 
	int nfront = queue.gettop();
	printf("队列头:%d", nfront);

	// 出队
	queue.pop();
	queue.pop();
	queue.pop();
	queue.pop();
	queue.pop();
	queue.pop();
	queue.pop();
	queue.pop();
	queue.pop();
	queue.pop();
	queue.pop();
	queue.pop();

	// 是否为空
	bool result = queue.empty();
	printf("队列是否为空:%d\n", result);

	//
	return 0;
}